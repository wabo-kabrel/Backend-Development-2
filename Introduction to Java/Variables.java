// Variables and Data Types
/* Java is statically typed, so you must declare a variable's
 type before using it.
 */
public class Variables {
    public static void main(String[] args) {
        int age = 25;
        double price = 19.99;
        float average = 9.8f;
        boolean isStudent = true;
        char grade = 'A';
        String name = "Kabrel";

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Price: $" + price);
        System.out.println("Average: " + average);
        System.out.println("Student: " + isStudent);
        System.out.println("Grade: " + grade);
    }
}

// Note that for strings, the type "String" is used with a capital S.
/* Note that for floating point numbers, the type "float" is used with a lowercase f
 at the end of the value assigned to the floating point variable.
*/

